N=10;M=15;
nx=0:N-1;
x=[nx<=5];
n1=0:11;
h1=0.8.^n1;
nh=0:M-1;
h=zeros(1,M);
h(find(nh<=11))=h1;
y1=conv(x,h);
n01=min(nx)+min(nh);
n02=max(nx)+max(nh);
ny1=[n01:n02];
subplot(321),stem(nx,x,'.');
text(8,0.7,'x(n)');
subplot(322),stem(nh,h,'.');
text(11,0.7,'h(n)');
subplot(312),stem(ny1,y1,'.');
text(18,3,'直接线性卷积');
n=0:N+M-2;
L=length(n);
xk=fft(x,L);
hk=fft(h,L);
yk=xk.*hk;
y2=ifft(yk);
y2=abs(y2);
subplot(313),stem(n,y2,'.');
text(18,3,'用FFT进行卷积');